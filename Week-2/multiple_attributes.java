 public class multiple_attributes {
    String fname = "John";
    String lname = "Doe";
    int age = 24;
  
    public static void main(String[] args)
     {
        multiple_attributes myObj = new multiple_attributes();
      System.out.println("Name: " + myObj.fname + " " + myObj.lname);
      System.out.println("Age: " + myObj.age);
    }
  }
   